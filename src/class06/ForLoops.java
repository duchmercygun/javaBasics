package class06;

public class ForLoops {
    public static void main(String[] args) {

        // these symbols use to increment number by 1/5/10

        int number=10;
        number=number+1;
        System.out.println(number); // 11
        number+=1;
        System.out.println(number);// 12
        number++;
        System.out.println(number); // 13

        int number2=10;
        number2--;
        System.out.println(number2); // to decrement values
    }
}
